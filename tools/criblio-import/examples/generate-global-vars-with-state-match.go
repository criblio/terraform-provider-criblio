package main

import (
	"context"
	"fmt"
	"log"
	"os"
	"strings"

	"github.com/criblio/terraform-provider-criblio/internal/sdk"
	"github.com/criblio/terraform-provider-criblio/internal/sdk/models/operations"
	"github.com/criblio/terraform-provider-criblio/tools/criblio-import/internal/terraform"
)

func main() {
	ctx := context.Background()

	fmt.Println("🔐 Initializing SDK client (authentication handled automatically)...")
	client := sdk.New()

	// Fetch global vars for a specific group
	groupID := "default"
	if len(os.Args) > 1 {
		groupID = os.Args[1]
	}

	fmt.Printf("📥 Fetching global variables for group: %s...\n", groupID)

	request := operations.GetGlobalVariableRequest{
		GroupID: groupID,
	}

	res, err := client.GlobalVariables.GetGlobalVariable(ctx, request)
	if err != nil {
		log.Fatalf("❌ Failed to fetch global variables: %v", err)
	}

	if res.StatusCode != 200 || res.Object == nil {
		log.Fatalf("❌ Unexpected response: status %d", res.StatusCode)
	}

	globalVars := res.Object.Items
	fmt.Printf("\n✅ Found %d global variables\n\n", len(globalVars))

	if len(globalVars) == 0 {
		fmt.Println("No global variables found. Nothing to generate.")
		return
	}

	// Step 1: Generate minimal Terraform resources (only required fields)
	fmt.Println("📝 Generating minimal Terraform resources (only required fields)...")

	var terraformResources strings.Builder
	terraformResources.WriteString("# Generated by criblio-import tool\n")
	terraformResources.WriteString("# Global variables exported from Cribl\n")
	terraformResources.WriteString("# Minimal config - only required fields (id, group_id)\n")
	terraformResources.WriteString("# After import, run: terraform plan -generate-config-out=complete.tf\n\n")

	importSpecs := []terraform.ImportSpec{}

	for _, gv := range globalVars {
		if gv.ID == "" {
			continue
		}

		resourceName := normalizeResourceName(gv.ID)
		terraformResources.WriteString(fmt.Sprintf("resource \"criblio_global_var\" \"%s\" {\n", resourceName))
		terraformResources.WriteString(fmt.Sprintf("  id       = %q\n", gv.ID))
		terraformResources.WriteString(fmt.Sprintf("  group_id = %q\n", groupID))
		terraformResources.WriteString("}\n\n")

		// Track for import
		importSpecs = append(importSpecs, terraform.ImportSpec{
			ResourceType: "criblio_global_var",
			ResourceName: resourceName,
			ImportID:     fmt.Sprintf(`{"group_id": %q, "id": %q}`, groupID, gv.ID),
		})
	}

	// Write to file
	outputFile := "global-vars.tf"
	err = os.WriteFile(outputFile, []byte(terraformResources.String()), 0644)
	if err != nil {
		log.Fatalf("❌ Failed to write Terraform file: %v", err)
	}

	fmt.Printf("✅ Generated minimal Terraform file: %s\n", outputFile)
	fmt.Printf("📊 Summary: %d global variables exported\n", len(globalVars))

	// Step 2: Auto-import if requested
	autoImport := os.Getenv("AUTO_IMPORT") == "true"

	if autoImport {
		fmt.Println("\n🔄 Auto-importing resources into Terraform state...")

		// Create provider.tf if it doesn't exist
		if _, err := os.Stat("provider.tf"); os.IsNotExist(err) {
			providerConfig := `terraform {
  required_providers {
    criblio = {
      source  = "criblio/criblio"
    }
  }
}

provider "criblio" {
  # Authentication via environment variables or ~/.cribl/credentials
}
`
			os.WriteFile("provider.tf", []byte(providerConfig), 0644)
			fmt.Println("  ✓ Created provider.tf")
		}

		// Initialize Terraform importer
		importer, err := terraform.NewImporter(".")
		if err != nil {
			log.Fatalf("❌ Failed to create Terraform importer: %v", err)
		}

		// Initialize Terraform
		if err := importer.Init(ctx); err != nil {
			log.Fatalf("❌ Failed to initialize Terraform: %v", err)
		}

		// Import all resources
		if err := importer.ImportMultipleResources(ctx, importSpecs); err != nil {
			log.Printf("⚠️  Some imports may have failed: %v", err)
		}

		// Refresh state
		fmt.Println("")
		fmt.Println("🔄 Refreshing state to populate all fields...")
		if err := importer.Refresh(ctx); err != nil {
			log.Printf("⚠️  Refresh warning: %v", err)
		}

		// Generate config from state (this matches what's actually in state)
		fmt.Println("")
		fmt.Println("📝 Generating config from state to match exactly...")
		fmt.Println("   (This ensures no updates after import)")
		
		// Use terraform-exec to run plan with generate-config-out
		// Note: terraform-exec doesn't directly support -generate-config-out
		// So we'll need to use a workaround or document the manual step
		fmt.Println("   Run manually: terraform plan -generate-config-out=global-vars-complete.tf")
		fmt.Println("   Then replace global-vars.tf with the generated file")

		fmt.Println("\n💡 Next steps:")
		fmt.Println("   1. Review the state: terraform show")
		fmt.Println("   2. Generate config from state: terraform plan -generate-config-out=complete.tf")
		fmt.Println("   3. Review the plan: terraform plan")
	} else {
		fmt.Println("\n💡 Next steps:")
		fmt.Println("   1. Review the generated global-vars.tf file")
		fmt.Println("   2. Run: terraform init")
		fmt.Println("   3. Import: terraform import criblio_global_var.<name> '{\"group_id\": \"default\", \"id\": \"<var_id>\"}'")
		fmt.Println("   4. Run: terraform refresh")
		fmt.Println("   5. Generate matching config: terraform plan -generate-config-out=complete.tf")
		fmt.Println("   6. Replace global-vars.tf with complete.tf for exact state match")
	}
}

func normalizeResourceName(name string) string {
	name = strings.ReplaceAll(name, "-", "_")
	name = strings.ReplaceAll(name, ".", "_")
	name = strings.ReplaceAll(name, " ", "_")

	if len(name) > 0 && !isLetter(rune(name[0])) {
		name = "r_" + name
	}

	return name
}

func isLetter(r rune) bool {
	return (r >= 'a' && r <= 'z') || (r >= 'A' && r <= 'Z')
}

