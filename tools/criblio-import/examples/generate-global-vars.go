package main

import (
	"context"
	"fmt"
	"log"
	"os"
	"strings"

	"github.com/criblio/terraform-provider-criblio/internal/sdk"
	"github.com/criblio/terraform-provider-criblio/internal/sdk/models/operations"
)

func main() {
	ctx := context.Background()

	fmt.Println("🔐 Initializing SDK client (authentication handled automatically)...")
	client := sdk.New()

	// Fetch global vars for a specific group
	groupID := "default"
	if len(os.Args) > 1 {
		groupID = os.Args[1]
	}

	fmt.Printf("📥 Fetching global variables for group: %s...\n", groupID)

	request := operations.GetGlobalVariableRequest{
		GroupID: groupID,
	}

	res, err := client.GlobalVariables.GetGlobalVariable(ctx, request)
	if err != nil {
		log.Fatalf("❌ Failed to fetch global variables: %v", err)
	}

	if res.StatusCode != 200 || res.Object == nil {
		log.Fatalf("❌ Unexpected response: status %d", res.StatusCode)
	}

	globalVars := res.Object.Items
	fmt.Printf("\n✅ Found %d global variables\n\n", len(globalVars))

	if len(globalVars) == 0 {
		fmt.Println("No global variables found. Nothing to generate.")
		return
	}

	// Step 2: Generate Terraform resources
	// Generate MINIMAL config (only required fields) to match what's in state after import
	// This prevents "update" warnings after import since the provider's Read function
	// only populates the `items` computed field, not individual fields
	fmt.Println("📝 Generating minimal Terraform resources (only required fields)...")
	fmt.Println("   Note: After import, run 'terraform plan -generate-config-out=complete.tf' to get full config")

	var terraformResources strings.Builder
	terraformResources.WriteString("# Generated by criblio-import tool\n")
	terraformResources.WriteString("# Global variables exported from Cribl\n")
	terraformResources.WriteString("# Minimal config - only required fields (id, group_id)\n")
	terraformResources.WriteString("# After import, run: terraform plan -generate-config-out=complete.tf\n\n")

	for _, gv := range globalVars {
		if gv.ID == "" {
			continue
		}

		resourceName := normalizeResourceName(gv.ID)
		terraformResources.WriteString(fmt.Sprintf("resource \"criblio_global_var\" \"%s\" {\n", resourceName))
		terraformResources.WriteString(fmt.Sprintf("  id       = %q\n", gv.ID))
		terraformResources.WriteString(fmt.Sprintf("  group_id = %q\n", groupID))
		
		// Include type with default value to match state behavior
		// State will have type populated from the API, but if not present, defaults to "any"
		// Since Read function doesn't populate individual fields, we use default to match
		typeValue := "any"
		if gv.Type != nil && *gv.Type != "" {
			typeValue = string(*gv.Type)
		}
		terraformResources.WriteString(fmt.Sprintf("  type     = %q  # Default value to match state\n", typeValue))
		
		terraformResources.WriteString("}\n\n")
	}

	// Write to file
	outputFile := "global-vars.tf"
	err = os.WriteFile(outputFile, []byte(terraformResources.String()), 0644)
	if err != nil {
		log.Fatalf("❌ Failed to write Terraform file: %v", err)
	}

	fmt.Printf("✅ Generated Terraform file: %s\n", outputFile)
	fmt.Printf("📊 Summary: %d global variables exported\n", len(globalVars))
	fmt.Println("")
	fmt.Println("💡 Next steps:")
	fmt.Println("   1. Review the generated global-vars.tf file")
	fmt.Println("   2. Run: terraform init")
	fmt.Println("   3. Import all: ./import-and-match-state.sh")
	fmt.Println("      OR manually:")
	fmt.Println("      - terraform import criblio_global_var.<name> '{\"group_id\": \"default\", \"id\": \"<var_id>\"}'")
	fmt.Println("      - terraform refresh")
	fmt.Println("      - terraform plan -generate-config-out=complete.tf")
	fmt.Println("      - Replace global-vars.tf with complete.tf")
	fmt.Println("   4. Run: terraform plan (should show minimal changes)")
	fmt.Println("")
	fmt.Println("📌 Note: The 'items' computed field may show changes - this is expected")
	fmt.Println("   and won't actually modify the resource.")
}

func normalizeResourceName(name string) string {
	name = strings.ReplaceAll(name, "-", "_")
	name = strings.ReplaceAll(name, ".", "_")
	name = strings.ReplaceAll(name, " ", "_")

	if len(name) > 0 && !isLetter(rune(name[0])) {
		name = "r_" + name
	}

	return name
}

func isLetter(r rune) bool {
	return (r >= 'a' && r <= 'z') || (r >= 'A' && r <= 'Z')
}

